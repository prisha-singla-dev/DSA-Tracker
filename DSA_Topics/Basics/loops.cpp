#include<bits/stdc++.h>
using namespace std;    
int main(){
    // For loop
    for(int i =0;i<=5;i++){
        cout<<"Hello World"<<endl;
    }
    cout<<"Loop Ended"<<endl;

    // While loop
    int i=0;
    while(i<=5){
        cout<<"Hello World"<<endl;
        i++;
    }

    // Do-While loop
    i=0;
    do{
        cout<<"Hello World"<<endl;
        i++;
    }while(i<=5);

    return 0; 
}